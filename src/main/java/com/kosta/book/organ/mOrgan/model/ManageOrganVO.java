package com.kosta.book.organ.mOrgan.model;

public class ManageOrganVO {
	private String title, com, isbn;
	private int ebook_reqNo;
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getCom() {
		return com;
	}
	public void setCom(String com) {
		this.com = com;
	}
	public int getEbook_reqNo() {
		return ebook_reqNo;
	}
	public void setEbook_reqNo(int ebook_reqNo) {
		this.ebook_reqNo = ebook_reqNo;
	}
	public String getIsbn() {
		return isbn;
	}
	public void setIsbn(String isbn) {
		this.isbn = isbn;
	}
	
	
}
